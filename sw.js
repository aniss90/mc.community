// تفعيل (activate) وتنظيف الكاش القديم
self.addEventListener('activate', event => {
    console.log("✅ Service Worker: Activated, cleaning old caches...");
    event.waitUntil(
        caches.keys().then(keys => {
            return Promise.all(
                keys.filter(k => k !== CACHE_NAME) // حافظ فقط على الكاش الحالي
                .map(k => {
                    console.log("🗑️ حذف الكاش القديم:", k);
                    return caches.delete(k);
                })
            );
        })
    );
});
// جلب الملفات (fetch)
self.addEventListener('fetch', event => {
    event.respondWith(
        caches.match(event.request).then(res => {
            // إذا الملف موجود في الكاش → رجعه
            if (res) return res;
            
            // إذا مش موجود → نزله وخزنه في الكاش
            return fetch(event.request).then(response => {
                return caches.open(CACHE_NAME).then(cache => {
                    cache.put(event.request, response.clone());
                    return response;
                });
            });
        })
    );
});

